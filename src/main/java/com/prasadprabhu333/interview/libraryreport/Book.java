package com.prasadprabhu333.interview.libraryreport;

/**
 *
 * This domain object represents a book which has a name and an author.
 */
public class Book {
    public String name;

    public String author;

    public Book() {
    }

    public Book(String name, String author) {
        this.name = name;
        this.author = author;
    }
}
